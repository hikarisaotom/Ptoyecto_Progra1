    private void btnG2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnG2ActionPerformed
        int i = 0, j = 1;
        boolean op = Asientos.Asientos(G2);
        String asiento = Asientos.asiento(matrizAsientos, i, j);;
        if (op == true) {
            if (contadorBoletos < boletos) {
                contadorBoletos++;
                btnG2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagenes/2.png")));
                G2 = true;
                lblAsientos.setVisible(true);

                asientos += "\n" + asiento;

                txtarea.setText(asientos);
                txtarea.disable();

            } else if (contadorBoletos == boletos) {
                JOptionPane.showMessageDialog(null, "Ya se seleccionaron los boletos!");
            }

        } else if (op == false) {
            btnG2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagenes/1.png")));
            G2 = false;
            asientos = Asientos.QuitarAsiento(asiento, asientos);

            if (asientos.length() == 0) {
                lblAsientos.setVisible(false);

                txtarea.setText(asientos);
            } else {

                txtarea.setText(asientos);
            }
            contadorBoletos--;
        }// fin condicion
        salidas = asientos;
    }//GEN-LAST:event_btnG2ActionPerformed

